    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRow = workRequestJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please Select a Patient");

        }
        
        PatientDoctorWorkRequest pat =(PatientDoctorWorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
        pat.setRespondStatus("Processing");
        
        RespondJPanel panel=new RespondJPanel(userProcessContainer, enterprise, pat);
        userProcessContainer.add("RespondfromDoctor", panel);
        CardLayout layout= (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_jButton1ActionPerformed

