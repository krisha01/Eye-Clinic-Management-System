    private void requestTestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a Row","Warning",JOptionPane.WARNING_MESSAGE);
        }else{
            PatientDoctorWorkRequest pat = (PatientDoctorWorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
            CardLayout lt = (CardLayout) userProcessContainer.getLayout();
            userProcessContainer.add("RequestandViewTestJPanel",new RequestandViewTestJPanel(userProcessContainer,userAccount,organization,enterprise,pat));
            lt.next(userProcessContainer);
        }
    }//GEN-LAST:event_requestTestJButtonActionPerformed

