    public void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();

        model.setRowCount(0);
       
        if(userAccount.getWorkQueue().getWorkRequestList()!=null){
            for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()) {
                if (request != null) {
                    PatientDoctorWorkRequest pat = (PatientDoctorWorkRequest) request;
                    if (pat.getDoctor() == userAccount) {
                        Object[] row = new Object[6];
                        row[0] = pat;
                        row[1] = pat.getPatientName();
                        row[2] = pat.getNormalOrNot();
                        row[3] = pat.getLabStatus() == null ? null : pat.getLabStatus();
                        row[4] = pat.getPreStatus() == null ? null : pat.getPreStatus();
                        row[5] = pat.getRespondStatus() == null ? null : pat.getRespondStatus();

                        model.addRow(row);
                    }
                }
            }
        }
    }

