    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Pattern pEmail = Pattern.compile("^([\\w]+)@([\\w]+)\\.([\\w]+)$");
        Matcher mMail = pEmail.matcher(email.getText());
        boolean isValidEmail = mMail.matches();

        if ((age.getText().length() == 0) || (address.getText().length() == 0) || (city.getText().length() == 0) || (email.getText().length() == 0) || (number.getText().length() == 0) || isValidEmail == false) {
            JOptionPane.showMessageDialog(null, "All fiends must be filled.");
        } else {
            try {

                //String name1= patientName.getText();
                int age1 = Integer.parseInt(age.getText());
                String address1 = address.getText();
                String city1 = city.getText();
                String email1 = email.getText();
                int phonenumber = Integer.parseInt(number.getText());

                Patient patient = patientOrganization.getPatientDirectory().addPatient(userAccount.getName());

                System.out.println(userAccount.getName());
                userAccount.setPatient(patient);
                patientName.setText(userAccount.getName());
                patient.setAge(age1);
                patient.setAddress(address1);
                patient.setCity(city1);
                patient.setPhoneNumber(phonenumber);
                patient.setEmail(email1);
                patient.setPrefdoctor((UserAccount) preferredDoc.getSelectedItem());
                patient.setPrefDoctor(String.valueOf(preferredDoc.getSelectedItem()));

                JOptionPane.showMessageDialog(null, "Your profile was created successfully!");

                age.setText("");
                patientName.setText("");
                address.setText("");
                number.setText("");
                email.setText("");
                city.setText("");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Please fill all the fields!");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Patient patient = patientOrganization.getPatientDirectory().addPatient(userAccount.getName());
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("JPG & GIF Images", "jpg", "gif");
        chooser.setFileFilter(filter);
        int returnVal = chooser.showOpenDialog(null);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File selectedFile = chooser.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            label.setIcon(Resize(path));
            patient.setPicture(path);
        }

    }//GEN-LAST:event_jButton1ActionPerformed


