    public void populateDoctor() {
        preferredDoc.removeAllItems();
        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (org instanceof DoctorOrganization) {
                for (UserAccount userAcc : org.getUserAccountDirectory().getUserAccountList()) {
                    if (userAcc.getRole() instanceof DoctorRole) {
                        preferredDoc.addItem(userAcc);
                    }
                }
            }
        }
    }

    public ImageIcon Resize(String ImagePath) {
        ImageIcon InfoPicture = new ImageIcon(ImagePath);
        Image img = InfoPicture.getImage();
        Image newImg = img.getScaledInstance(50, 50, Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }

