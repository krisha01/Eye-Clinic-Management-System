    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = phartbl.getSelectedRow();
        
        if(selectedRow <0){
            JOptionPane.showMessageDialog(null, "Please select one row!");
        }else{
            PatientDoctorWorkRequest request = (PatientDoctorWorkRequest)phartbl.getValueAt(selectedRow, 0);
            request.setRecieverEnterprise(enterprise);
            populateMedicine();
            JOptionPane.showMessageDialog(null, "Sucessfully assigned to this "+enterprise);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

