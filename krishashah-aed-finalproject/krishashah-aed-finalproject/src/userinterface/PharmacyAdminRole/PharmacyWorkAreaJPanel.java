    public void populateMedicine(){
        DefaultTableModel mod =(DefaultTableModel) phartbl.getModel();
        
        mod.setRowCount(0);
        if(userAccount.getWorkQueue().getWorkRequestList()!=null){
            for(WorkRequest req: organization.getWorkQueue().getWorkRequestList()){
                if(req != null){
                    PatientDoctorWorkRequest pat=(PatientDoctorWorkRequest)req;
                    Object[] row = new Object[6];
                    row[0]=pat;
                    row[1]=pat.getMedicineName();
                    row[2]=pat.getMedicineQuantity();
                    row[3]=pat.getSender();
                    row[4]=pat.getRecieverEnterprise();
                    row[5]=pat.getPreStatus();
                    
                    mod.addRow(row);
                }
            }
        }
    }
